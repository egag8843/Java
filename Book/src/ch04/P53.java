package ch04;

public class P53 {
	public static void main(String[] args) {
		
		int number1 = 10;
		System.out.println("number1 = 10 -> " + number1);
		
		number1 += 10;
		System.out.println("number1 += 10 -> " + number1);
		
		number1 -= 10;
		System.out.println("number1 -= 10 -> " + number1);
		
		number1 *= 10;
		System.out.println("number1 *= 10 -> " + number1);
		
		number1 /= 10;
		System.out.println("number1 /= 10 -> " + number1);
		
		number1 %= 10;
		System.out.println("number1 %= 10 -> " + number1);
	}
}
